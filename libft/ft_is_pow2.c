#include "libft.h"

int	ft_is_pow2(int nbr)
{
	int	count;

	if (nbr < 1)
		return (0);
	count = 0;
	while (nbr)
	{
		if (nbr & 1)
			count++;
		nbr >>= 1;
	}
	return (count == 1);
}
